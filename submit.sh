#!/usr/bin/env bash

set -euo pipefail

# -----------------------------------------------------------------------------
# submit.sh
# -----------------------------------------------------------------------------
# Назначение
# ----------
# Этот скрипт предназначен для максимально простого локального цикла работы с
# Fortran-проектом в VS Code:
#
# 1. Открыть проект в VS Code.
# 2. При необходимости поправить флаги компиляции в секции COMPILER_FLAGS.
# 3. Запустить скрипт одной командой:
#      ./submit.sh
# 4. Скрипт сам:
#      - перейдет в директорию проекта;
#      - создаст папку сборки build, если ее еще нет;
#      - скомпилирует Fortran-исходники компилятором ifx;
#      - запустит полученный исполняемый файл.
#
# То есть этот файл заменяет собой "ручную" последовательность команд компиляции
# и запуска и сводит работу к одному действию.
#
#
# Почему скрипт называется submit.sh, хотя он больше не SLURM-submit
# ---------------------------------------------------------------
# Ранее файл был похож на batch-скрипт для кластера (с директивами #SBATCH и
# загрузкой модулей окружения). Для локальной работы в VS Code это обычно
# неудобно, потому что:
#
# - локально не нужен планировщик SLURM;
# - локально обычно нет команды module load;
# - хочется одной командой именно "собрать и сразу запустить";
# - хочется удобно редактировать флаги компиляции без переписывания длинной
#   командной строки.
#
# Поэтому скрипт был адаптирован под локальный сценарий разработки.
#
#
# Основная идея организации файла
# -------------------------------
# Скрипт разбит на логические части:
#
# - определение директории проекта;
# - выбор компилятора;
# - задание папки сборки и имени итогового бинарника;
# - перечисление флагов компиляции в удобном для редактирования виде;
# - перечисление исходных файлов в корректном порядке;
# - компиляция;
# - запуск программы.
#
# Такое разделение удобно тем, что обычно пользователю нужно менять только две
# секции:
#
# - COMPILER_FLAGS
# - FORTRAN_SOURCES
#
# Все остальное можно оставить как есть.
#
#
# Использование
# -------------
# Базовый запуск:
#
#     ./submit.sh
#
# Запуск с передачей аргументов программе:
#
#     ./submit.sh arg1 arg2 arg3
#
# В этом случае:
# - скрипт сначала выполнит компиляцию;
# - затем запустит собранную программу;
# - аргументы arg1 arg2 arg3 будут переданы уже самой Fortran-программе.
#
#
# Использование другого компилятора
# ---------------------------------
# По умолчанию скрипт использует компилятор ifx. Однако при желании можно
# временно подставить другой компилятор через переменную окружения FC:
#
#     FC=gfortran ./submit.sh
#
# Это полезно, если:
# - на конкретной машине ifx недоступен;
# - нужно быстро сравнить поведение с другим компилятором;
# - проект временно тестируется в другой среде.
#
#
# Как редактировать флаги компиляции
# ----------------------------------
# Флаги вынесены в массив COMPILER_FLAGS. Это сделано специально для удобства.
# Вместо одной длинной строки каждый флаг записан на своей строке:
#
#     COMPILER_FLAGS=(
#       -O3
#       -xHost
#     )
#
# Преимущества такого подхода:
#
# - проще читать;
# - проще включать и выключать отдельные флаги;
# - меньше шанс случайно сломать команду компиляции;
# - удобнее пользоваться комментариями.
#
# Например, можно временно переключиться на отладочную сборку:
#
#     COMPILER_FLAGS=(
#       -O0
#       -g
#       -traceback
#       -check
#       -warn
#     )
#
# Или смешать оптимизацию и диагностику, если это поддерживает ваш сценарий.
#
#
# Почему исходники перечислены явно и в определенном порядке
# ----------------------------------------------------------
# В Fortran порядок компиляции файлов может быть важен, особенно если:
#
# - одни файлы объявляют модули;
# - другие файлы используют эти модули через use.
#
# Если передать файлы в неправильном порядке, компиляция может завершиться
# ошибкой, потому что нужный .mod-файл еще не будет создан к моменту обработки
# зависимого исходника.
#
# Именно поэтому массив FORTRAN_SOURCES не собирается автоматически по маске
# *.f90, а задается вручную в явном порядке. Это надежнее и предсказуемее.
#
#
# Что находится в папке build
# ---------------------------
# В директорию build складываются:
#
# - итоговый исполняемый файл;
# - .mod-файлы Fortran-модулей, если компилятор их генерирует туда.
#
# Это позволяет:
#
# - не засорять корень проекта промежуточными файлами;
# - отделить исходники от результатов сборки;
# - упростить очистку проекта при необходимости.
#
#
# Поведение при ошибках
# ---------------------
# В начале файла включен строгий режим Bash:
#
#     set -euo pipefail
#
# Это означает:
#
# - -e: завершать скрипт при первой ошибке команды;
# - -u: считать ошибкой обращение к необъявленной переменной;
# - pipefail: считать ошибкой сбой любой команды в конвейере.
#
# Такой режим полезен, потому что скрипт не "притворяется", будто все прошло
# успешно, если компиляция на самом деле завершилась с ошибкой.
#
#
# Работа из VS Code
# -----------------
# Этот скрипт хорошо подходит для запуска из VS Code:
#
# - из встроенного терминала;
# - из tasks.json;
# - из кнопки запуска пользовательской задачи;
# - через внешние расширения, умеющие запускать shell-скрипты.
#
# Ключевая особенность: скрипт сам переходит в директорию, где лежит он сам.
# Поэтому его можно вызывать не только из корня проекта, но и из другой текущей
# директории, и сборка все равно будет происходить относительно каталога проекта.
#
#
# Примечание по Windows
# ---------------------
# Поскольку файл имеет расширение .sh, предполагается запуск в Bash-совместимой
# среде, например:
#
# - Git Bash;
# - MSYS2;
# - WSL;
# - bash, доступный из VS Code terminal profile.
#
# Если VS Code открыт на Windows и используется PowerShell по умолчанию, сам
# скрипт все равно можно запускать через bash, например:
#
#     bash ./submit.sh
#
# Если же в VS Code терминал уже настроен на Bash, то достаточно:
#
#     ./submit.sh
#
# -----------------------------------------------------------------------------

# Определяем абсолютный путь к директории, в которой расположен сам скрипт.
# Это нужно, чтобы все дальнейшие относительные пути работали стабильно,
# независимо от того, из какой директории пользователь его вызвал.
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# Переходим в каталог проекта. После этого можно безопасно использовать
# относительные имена исходников и папки сборки.
cd "$SCRIPT_DIR"

# Выбор компилятора.
# Если переменная окружения FC задана снаружи, будет использовано ее значение.
# Иначе по умолчанию берется ifx.
#
# Примеры:
#   ./submit.sh
#   FC=ifx ./submit.sh
#   FC=gfortran ./submit.sh
COMPILER="${FC:-ifx}"

# Каталог, куда складываются промежуточные результаты сборки и итоговый бинарник.
BUILD_DIR="build"

# Имя исполняемого файла, которое будет создано после успешной компиляции.
OUTPUT_NAME="main"

# Список флагов компиляции.
# Каждый флаг записан на отдельной строке, чтобы их было удобно:
# - читать;
# - комментировать;
# - временно включать и выключать;
# - переставлять местами;
# - дополнять новыми ключами.
#
# Текущий набор:
# -O3    : агрессивная оптимизация производительности;
# -xHost : оптимизация под архитектуру текущего процессора.
#
# При отладке можно временно заменить, например, на:
#   -O0
#   -g
#   -traceback
#   -check
#   -warn
COMPILER_FLAGS=(
  -O3
  -xHost
)

# Список исходных файлов проекта.
# Порядок здесь важен: для Fortran-модулей зависимые файлы должны компилироваться
# после тех файлов, которые предоставляют нужные модули.
#
# Если в проект будут добавлены новые исходники, их лучше явно вписывать сюда, а
# не пытаться автоматически собирать список по шаблону *.f90. Это дает полный
# контроль над порядком компиляции и уменьшает количество трудноуловимых ошибок.
FORTRAN_SOURCES=(
  subgrid_boundary_initializer.f90
  macrogrid_boundary_initializer.f90
  subgrid_solver.f90
  macrogrid_solver.f90
  main.f90
)

# Все аргументы, с которыми был вызван скрипт, сохраняются и затем передаются
# уже в собранную программу.
# Это удобно, если ваша Fortran-программа принимает параметры командной строки.
RUN_ARGS=("$@")

# Создаем каталог сборки, если он еще отсутствует.
# Команда безопасна при повторных запусках.
mkdir -p "$BUILD_DIR"

# Небольшие диагностические сообщения, чтобы в терминале VS Code было сразу
# видно, какой компилятор используется и куда собирается бинарник.
echo "Compiler: $COMPILER"
echo "Building: $BUILD_DIR/$OUTPUT_NAME"

# Основная команда компиляции.
# Здесь:
# - подставляется выбранный компилятор;
# - передаются все флаги из массива COMPILER_FLAGS;
# - указывается каталог для .mod-файлов через -module;
# - перечисляются исходники в нужном порядке;
# - задается имя итогового исполняемого файла через -o.
"$COMPILER" \
  "${COMPILER_FLAGS[@]}" \
  -module "$BUILD_DIR" \
  "${FORTRAN_SOURCES[@]}" \
  -o "$BUILD_DIR/$OUTPUT_NAME"

# После успешной компиляции сразу запускаем программу.
# Если при вызове submit.sh были переданы аргументы, они прокидываются в бинарник.
#
# Пример:
#   ./submit.sh input.dat 100
#
# Фактически приведет к запуску вида:
#   ./build/main input.dat 100
echo "Running: $BUILD_DIR/$OUTPUT_NAME ${RUN_ARGS[*]:-}"
"$SCRIPT_DIR/$BUILD_DIR/$OUTPUT_NAME" "${RUN_ARGS[@]}"
